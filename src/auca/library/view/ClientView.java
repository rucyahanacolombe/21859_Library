    private void PopulateClient() {
        ClientDao cdao = new ClientDao();
        List<Client> cl = cdao.getClient();
        String[] col = {"Reg No", "First Name", "Last Name", "Email Address", "Phone Number", "Client Category", "Passport"};
        DefaultTableModel tm = (DefaultTableModel) table.getModel();
        tm.setRowCount(0);
        tm.setColumnIdentifiers(col);
        for (Iterator iter = cl.iterator(); iter.hasNext();) {
            Client c = (Client) iter.next();
            Object[] client = {c.getRegno(), c.getFirstname(), c.getLastname(), c.getAddress(), c.getPhoneno(), c.getClientcategory(), c.getPhoto()};
            tm.addRow(client);

        }
    }


